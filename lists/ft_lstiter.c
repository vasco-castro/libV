#include "../inc/libft.h"

/// @brief Iterates through the list ’lst’ and applies the
/// function ’f’ to the content of each node.
/// @param lst
/// @param f
void ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst && f)
	{
		f(lst->content);
		lst = lst->next;
	}
}
